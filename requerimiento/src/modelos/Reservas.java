package modelos;

public class Reservas {
	
	private int id;
	private int lugarIda;
	private int lugarVuelta;
	private String ida;
	private String vuelta;

	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getLugarIda() {
		return lugarIda;
	}
	public void setLugarIda(int lugarIda) {
		this.lugarIda = lugarIda;
	}
	public int getLugarVuelta() {
		return lugarVuelta;
	}
	public void setLugarVuelta(int lugarVuelta) {
		this.lugarVuelta = lugarVuelta;
	}
	public String getIda() {
		return ida;
	}
	public void setIda(String ida) {
		this.ida = ida;
	}
	public String getVuelta() {
		return vuelta;
	}
	public void setVuelta(String vuelta) {
		this.vuelta = vuelta;
	}
	
}
